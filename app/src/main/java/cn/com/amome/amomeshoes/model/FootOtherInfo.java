package cn.com.amome.amomeshoes.model;

/**
 * 存放脚其他疾病信息
 */
public class FootOtherInfo {
	private String type, name;

	public void setType(String type) {
		this.type = type;
	}

	public String getType() {
		return type;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}
}
