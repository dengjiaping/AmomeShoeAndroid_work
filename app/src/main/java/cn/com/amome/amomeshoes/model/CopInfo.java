package cn.com.amome.amomeshoes.model;

/**
 * cop点坐标信息
 */
public class CopInfo {
	private float x; // cop横坐标
	private float y; // cop纵坐标

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}
}
